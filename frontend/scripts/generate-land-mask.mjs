/**
 * Generates components/globe/landMask.ts — a 720x360 (0.5°) land/water
 * bitmask rasterized from Natural Earth 50m land polygons (world-atlas).
 *
 * Run once when the mask needs regenerating:
 *   node scripts/generate-land-mask.mjs
 */
import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const require = createRequire(import.meta.url);
const topojson = require("topojson-client");
const land50m = require("world-atlas/land-50m.json");

const WIDTH = 720; // 0.5° per cell in longitude
const HEIGHT = 360; // 0.5° per cell in latitude

const land = topojson.feature(land50m, land50m.objects.land);

// Collect every ring (outer boundaries and holes alike — even-odd parity
// handles both uniformly).
const rings = [];
for (const feature of land.features ?? [land]) {
  const geom = feature.geometry;
  const polys =
    geom.type === "Polygon" ? [geom.coordinates] : geom.coordinates;
  for (const poly of polys) for (const ring of poly) rings.push(ring);
}

// Scanline fill: for each latitude row, find every longitude where a ring
// edge crosses that latitude, sort the crossings, and mark cells between
// odd/even pairs as land.
const mask = new Uint8Array((WIDTH * HEIGHT) / 8);
const rowCrossings = Array.from({ length: HEIGHT }, () => []);

for (const ring of rings) {
  for (let i = 0; i < ring.length - 1; i++) {
    const [x1, y1] = ring[i];
    const [x2, y2] = ring[i + 1];
    if (y1 === y2) continue;
    const yMin = Math.min(y1, y2);
    const yMax = Math.max(y1, y2);
    // Cell-center latitudes run from -89.75 to +89.75.
    const rowStart = Math.max(0, Math.ceil((yMin + 90) / 0.5 - 0.5));
    const rowEnd = Math.min(HEIGHT - 1, Math.floor((yMax + 90) / 0.5 - 0.5));
    for (let row = rowStart; row <= rowEnd; row++) {
      const lat = row * 0.5 - 90 + 0.25;
      // Half-open interval [yMin, yMax) so shared vertices count once.
      if (lat >= yMax) continue;
      const t = (lat - y1) / (y2 - y1);
      rowCrossings[row].push(x1 + t * (x2 - x1));
    }
  }
}

for (let row = 0; row < HEIGHT; row++) {
  const xs = rowCrossings[row].sort((a, b) => a - b);
  for (let k = 0; k + 1 < xs.length; k += 2) {
    const colStart = Math.max(0, Math.ceil((xs[k] + 180) / 0.5 - 0.5));
    const colEnd = Math.min(
      WIDTH - 1,
      Math.floor((xs[k + 1] + 180) / 0.5 - 0.5)
    );
    for (let col = colStart; col <= colEnd; col++) {
      const bit = row * WIDTH + col;
      mask[bit >> 3] |= 1 << (bit & 7);
    }
  }
}

// Sanity: land should cover roughly 29% of cell-area-weighted surface,
// and an ASCII preview lets a human confirm the continents look right.
let landCells = 0;
for (let i = 0; i < WIDTH * HEIGHT; i++) {
  if (mask[i >> 3] & (1 << (i & 7))) landCells++;
}
console.log(
  `land cells: ${landCells} / ${WIDTH * HEIGHT} (${((landCells / (WIDTH * HEIGHT)) * 100).toFixed(1)}% of grid)`
);

const PREVIEW_W = 144;
const PREVIEW_H = 60;
for (let py = PREVIEW_H - 1; py >= 0; py--) {
  let line = "";
  for (let px = 0; px < PREVIEW_W; px++) {
    const col = Math.floor((px / PREVIEW_W) * WIDTH);
    const row = Math.floor((py / PREVIEW_H) * HEIGHT);
    const bit = row * WIDTH + col;
    line += mask[bit >> 3] & (1 << (bit & 7)) ? "#" : ".";
  }
  console.log(line);
}

const base64 = Buffer.from(mask).toString("base64");
const out = `/**
 * AUTO-GENERATED by scripts/generate-land-mask.mjs — do not edit by hand.
 *
 * A ${WIDTH}x${HEIGHT} (0.5° per cell) land/water bitmask rasterized from
 * Natural Earth 50m land polygons via the world-atlas package. Row 0 is the
 * southernmost band (lat -90..-89.5), column 0 the westernmost (lon -180..-179.5).
 */

const WIDTH = ${WIDTH};
const HEIGHT = ${HEIGHT};

const MASK_BASE64 =
${base64.match(/.{1,76}/g).map((s) => `  "${s}"`).join(" +\n")};

/* atob-free base64 decode so the module is safe to import anywhere. */
const B64_ALPHABET =
  "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";

function decodeBase64(input: string): Uint8Array {
  const clean = input.replace(/=+$/, "");
  const out = new Uint8Array(Math.floor((clean.length * 3) / 4));
  let buffer = 0;
  let bits = 0;
  let index = 0;
  for (let i = 0; i < clean.length; i++) {
    buffer = (buffer << 6) | B64_ALPHABET.indexOf(clean[i]);
    bits += 6;
    if (bits >= 8) {
      bits -= 8;
      out[index++] = (buffer >> bits) & 0xff;
    }
  }
  return out;
}

let mask: Uint8Array | null = null;

/** True if the given coordinate falls on land (Natural Earth 50m). */
export function isLand(lat: number, lon: number): boolean {
  if (!mask) mask = decodeBase64(MASK_BASE64);
  const row = Math.min(HEIGHT - 1, Math.max(0, Math.floor((lat + 90) * 2)));
  const col = Math.min(WIDTH - 1, Math.max(0, Math.floor((lon + 180) * 2)));
  const bit = row * WIDTH + col;
  return (mask[bit >> 3] & (1 << (bit & 7))) !== 0;
}
`;

const dest = join(
  dirname(fileURLToPath(import.meta.url)),
  "..",
  "components",
  "globe",
  "landMask.ts"
);
writeFileSync(dest, out);
console.log(`wrote ${dest} (${(out.length / 1024).toFixed(1)} KB)`);
